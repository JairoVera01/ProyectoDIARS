package model;

/**
 *
 * @author jairo
 */
public class Tipo_pedido {
    private int id;
    private String nombre;

    public Tipo_pedido() {
    }

    public Tipo_pedido(int id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
}
